package com.seekerr.simulator.robot.obstacleGenerator;

/**
 *
 * @author  Wesley Kerr
 */
public class Rectangle {
    
    public int x1;
    public int y1;
    public int x2;
    public int y2;
    
    /** Creates a new instance of Rectangle */
    public Rectangle(int x1, int y1, int x2, int y2) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
    }
    
}
